<?php

namespace App\Http\Controllers\Admin;

use App\Http\Controllers\Controller;
use App\Models\User;
use Illuminate\Http\Request;
use Illuminate\Support\Facades\Hash;
use Illuminate\Support\Facades\Mail;
use Illuminate\Support\Str;
use App\Mail\BloggerCreatedMail;

class BloggerManagementController extends Controller
{
    /**
     * Display listing of bloggers
     */
    public function index(Request $request)
    {
        $query = User::where('user_type', 'bloger');

        // Apply search filter
        if ($request->filled('search')) {
            $search = $request->search;
            $query->where(function($q) use ($search) {
                $q->where('name', 'like', "%{$search}%")
                  ->orWhere('email', 'like', "%{$search}%")
                  ->orWhere('phone', 'like', "%{$search}%");
            });
        }

        $bloggers = $query->latest()->paginate(15);

        return view('admin.bloggers.index', compact('bloggers'));
    }

    /**
     * Show form for creating a blogger
     */
    public function create()
    {
        return view('admin.bloggers.create');
    }

    /**
     * Store new blogger in database
     */
    public function store(Request $request)
    {
        $request->validate([
            'name' => 'required|string|max:255',
            'email' => 'required|email|unique:users,email',
            'phone' => 'required|string|max:20',
        ], [
            'email.unique' => 'This email is already registered on Mali Setu.',
        ]);

        try {
            // Generate autogenerated random password
            $plainPassword = Str::random(10);

            $blogger = User::create([
                'name' => $request->name,
                'email' => $request->email,
                'phone' => $request->phone,
                'password' => Hash::make($plainPassword),
                'user_type' => 'bloger',
                'blog_access' => true,
                'status' => 'active',
                'caste_verification_status' => 'approved', // Auto-approved to avoid restrictions
            ]);

            // Dispatch credentials email
            Mail::to($blogger->email)->send(new BloggerCreatedMail($blogger, $plainPassword));

            return redirect()->route('admin.bloggers.index')
                ->with('success', "Blogger '{$blogger->name}' created successfully! Credentials emailed. Temporary password: {$plainPassword}");
        } catch (\Exception $e) {
            return back()->withErrors(['error' => 'Failed to create blogger: ' . $e->getMessage()])->withInput();
        }
    }

    /**
     * View blogger details
     */
    public function show($id)
    {
        $blogger = User::where('user_type', 'bloger')->findOrFail($id);
        
        // Fetch blogger's articles
        $blogs = \App\Models\Blog::where('user_id', $blogger->id)->latest()->get();

        return view('admin.bloggers.show', compact('blogger', 'blogs'));
    }

    /**
     * Show form for editing blogger details
     */
    public function edit($id)
    {
        $blogger = User::where('user_type', 'bloger')->findOrFail($id);
        return view('admin.bloggers.edit', compact('blogger'));
    }

    /**
     * Update blogger details
     */
    public function update(Request $request, $id)
    {
        $blogger = User::where('user_type', 'bloger')->findOrFail($id);

        $request->validate([
            'name' => 'required|string|max:255',
            'email' => 'required|email|unique:users,email,' . $blogger->id,
            'phone' => 'required|string|max:20',
            'status' => 'required|in:active,suspended',
        ]);

        try {
            $blogger->update([
                'name' => $request->name,
                'email' => $request->email,
                'phone' => $request->phone,
                'status' => $request->status,
            ]);

            return redirect()->route('admin.bloggers.index')
                ->with('success', "Blogger '{$blogger->name}' updated successfully!");
        } catch (\Exception $e) {
            return back()->withErrors(['error' => 'Failed to update blogger: ' . $e->getMessage()])->withInput();
        }
    }

    /**
     * Delete blogger from database
     */
    public function destroy($id)
    {
        try {
            $blogger = User::where('user_type', 'bloger')->findOrFail($id);
            
            // Delete their published blogs
            \App\Models\Blog::where('user_id', $blogger->id)->delete();
            
            $blogger->delete();

            return redirect()->route('admin.bloggers.index')
                ->with('success', 'Blogger and their articles deleted successfully.');
        } catch (\Exception $e) {
            return redirect()->back()->with('error', 'Failed to delete blogger: ' . $e->getMessage());
        }
    }

    /**
     * Toggle status (active/suspended)
     */
    public function toggleStatus($id)
    {
        try {
            $blogger = User::where('user_type', 'bloger')->findOrFail($id);
            $newStatus = $blogger->status === 'active' ? 'suspended' : 'active';
            $blogger->update(['status' => $newStatus]);

            return response()->json([
                'success' => true,
                'status' => $newStatus,
                'message' => "Blogger status updated to " . ucfirst($newStatus)
            ]);
        } catch (\Exception $e) {
            return response()->json([
                'success' => false,
                'message' => $e->getMessage()
            ], 500);
        }
    }

    /**
     * Check if email exists in database (AJAX)
     */
    public function checkEmail(Request $request)
    {
        $request->validate(['email' => 'required|email']);
        
        $email = $request->email;
        $excludeId = $request->input('exclude_id');

        $exists = User::where('email', $email)
            ->when($excludeId, function($q) use ($excludeId) {
                $q->where('id', '!=', $excludeId);
            })
            ->exists();

        return response()->json([
            'exists' => $exists
        ]);
    }
}
